package Java_SubString_JAVA11;

import java.util.Scanner;

public class Main2 {

    public static void main(String[] args) {
        Scanner in = new Scanner(System.in);
        String S = in.next(); // Toheeb
        int start = in.nextInt();  // 0
        int end = in.nextInt();  // 1
        System.out.println(S.substring(start, end)); //  String.substring(int, int) 0,1 =
    } // Toheeb, 0 , Toheeb
}
